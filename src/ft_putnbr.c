#include "ft_printf.h"

int	ft_putnbr(int nb)
{
	static int	c = 0;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		c += (ft_putchar('-'));
		ft_putnbr(-nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		c += (ft_putchar(nb + '0'));
	return (c);
}
